package com.itn.core;

import java.util.Scanner;

/**
 *
 * @author SAURAV
 */
public class TenEvenNumbersUsingWhile {

    public static void main(String[] args) {
        
        int a = 0;
        
        System.out.println("Printing even numbers from 1 - 10");
        
        while (a < 10){
            a = a + 2;
            System.out.println(a);
            
        }
        
    }
}
